  /* ── LANG DETECTION ── */
  function detectLang() {
    if (typeof window !== 'undefined' && window.SF_CONFIG && window.SF_CONFIG.locale) {
      return window.SF_CONFIG.locale;
    }
    try {
      const saved = localStorage.getItem('sf-lang');
      if (saved && T[saved]) return saved;
    } catch (e) {}
    const browser = (navigator.language || '').toLowerCase();
    if (browser.indexOf('ko') === 0) return 'ko';
    if (browser.indexOf('fr') === 0) return 'fr';
    return 'en';
  }

  /* ── APPLY TRANSLATIONS ── */
  function applyLang(lang) {
    if (!T[lang]) lang = 'en';
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      const key = el.getAttribute('data-i18n');
      const fallback = T.en[key];
      const value = T[lang][key] != null ? T[lang][key] : fallback;
      if (value != null) el.textContent = value;
    });

    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      const key = el.getAttribute('data-i18n-html');
      const fallback = T.en[key];
      const value = T[lang][key] != null ? T[lang][key] : fallback;
      if (value != null) el.innerHTML = value;
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      const key = el.getAttribute('data-i18n-placeholder');
      const fallback = T.en[key];
      const value = T[lang][key] != null ? T[lang][key] : fallback;
      if (value != null) el.setAttribute('placeholder', value);
    });

    document.querySelectorAll('[data-i18n-sending]').forEach(function (el) {
      const key = el.getAttribute('data-i18n-sending');
      const fallback = T.en[key];
      const value = T[lang][key] != null ? T[lang][key] : fallback;
      if (value != null) el.setAttribute('data-label-sending', value);
    });

    try { localStorage.setItem('sf-lang', lang); } catch (e) {}
  }

  /* ── LANG SWITCHER ── */
  function bindSwitcher() {
    const toggles = document.querySelectorAll('#lang-toggle, #lang-toggle-mobile');
    toggles.forEach(function (btn) {
      if (!btn) return;
      btn.addEventListener('click', function () {
        const current = document.documentElement.lang || 'en';
        const next = current === 'ko' ? 'en' : current === 'en' ? 'fr' : 'ko';
        applyLang(next);
        toggles.forEach(function (b) {
          const c = document.documentElement.lang;
          b.textContent = c === 'ko' ? 'English' : c === 'en' ? 'Français' : '한국어';
        });
      });
    });

    // Initial button label
    const init = document.documentElement.lang;
    toggles.forEach(function (b) {
      if (!b) return;
      b.textContent = init === 'ko' ? 'English' : init === 'en' ? '한국어' : '한국어';
    });
  }

  /* ── BOOT ── */
  const lang = detectLang();
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', function () {
      applyLang(lang);
      bindSwitcher();
    });
  } else {
    applyLang(lang);
    bindSwitcher();
  }

  if (typeof window !== 'undefined') {
    window.SF_i18n = {
      apply: applyLang,
      current: function () { return document.documentElement.lang || detectLang(); },
      get: function (key) {
        var c = document.documentElement.lang || 'en';
        return (T[c] && T[c][key] != null) ? T[c][key] : (T.en[key] != null ? T.en[key] : key);
      }
    };
  }
